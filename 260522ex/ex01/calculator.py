def add(n1, n2):
    print(f"{n1} + {n2} = {n1 + n2}")


def sub(n1, n2):
    print(f"{n1} - {n2} = {n1 - n2}")


def mul(n1, n2):
    print(f"{n1} * {n2} = {n1 * n2}")


def div(n1, n2):
    print(f"{n1} / {n2} = {n1 / n2}")


def rest(n1, n2):
    print(f"{n1} % {n2} = {n1 % n2}")


def portion(n1, n2):
    print(f"{n1} // {n2} = {n1 // n2}")
